package academy.devdojo.maratonajava.introdução;

public class Aula11Vetores02 {
    public static void main(String[] args) {

        // Iteração com vetores

        String [] nomes = new String[3];

        nomes[0] = "Goku";
        nomes[1] = "Ichigo";
        nomes[2] = "Tanjiro";

        for (int i = 0; i < 3; i++) {
            System.out.println(nomes[i]);

        }

    }
}
